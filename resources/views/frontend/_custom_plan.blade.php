<h2 id="custom-plan">3. Custom plan</h2>
<p>
HeyHi allows Enterprise partners to create custom plans for their users. Not everyone has the same need, so you can provide your users with tailored plan for them. In each custom plan you will need to define the plan capacity which include:
</p>
<ul>
    <li>Number of guests per meeting</li>
    <li>Number of meeting rooms</li>
    <li>Number meeting minutes per month</li>
</ul>